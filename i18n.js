function getI18n(key) {
    let lang = 'es';
    try {
        const stored = localStorage.getItem('notifyUserProfile');
        if (stored) {
            const profile = JSON.parse(stored);
            if (profile.language) lang = profile.language;
        }
    } catch(e) {}
    return translations[lang] && translations[lang][key] ? translations[lang][key] : key;
}

function applyLanguage() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (el.tagName === 'INPUT' && (el.type === 'text' || el.type === 'search')) {
            el.placeholder = getI18n(key);
        } else {
            // Need to preserve inner HTML if there are icons, so check if there's an icon inside
            const icon = el.querySelector('i');
            if (icon) {
                // Remove all text nodes, keep the icon
                Array.from(el.childNodes).forEach(node => {
                    if (node.nodeType === 3) node.remove();
                });
                el.appendChild(document.createTextNode(' ' + getI18n(key)));
            } else {
                el.textContent = getI18n(key);
            }
        }
    });
    
    // Also trigger custom events or dynamic text updates
    if (window.onLanguageChanged) {
        window.onLanguageChanged();
    }
}
